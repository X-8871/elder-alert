esp_err_t VoicePrompt_PlayUploadOk(const bsp_max98357a_config_t *amp_config)
{
    if (amp_config == NULL) {
        return ESP_ERR_INVALID_ARG;
    }

    esp_err_t ret = BSP_MAX98357A_Init(amp_config);
    if (ret != ESP_OK) {
        return ret;
    }

    ret = BSP_MAX98357A_PlaySilence(VOICE_PROMPT_LEAD_SILENCE_MS);
    if (ret != ESP_OK) {
        ESP_LOGW(TAG, "voice prompt pre-silence failed: %s", esp_err_to_name(ret));
    }

    const size_t sample_count = sizeof(s_upload_ok_pcm) / sizeof(s_upload_ok_pcm[0]);
    ESP_LOGI(TAG, "play upload ok voice prompt samples=%u", (unsigned)sample_count);
    ret = BSP_MAX98357A_WriteMonoSamples(s_upload_ok_pcm,
                                         sample_count,
                                         1000);
    if (ret == ESP_OK) {
        esp_err_t silence_ret = BSP_MAX98357A_PlaySilence(VOICE_PROMPT_TAIL_SILENCE_MS);
        if (silence_ret != ESP_OK) {
            ESP_LOGW(TAG, "voice prompt tail-silence failed: %s", esp_err_to_name(silence_ret));
        }
    }

    esp_err_t deinit_ret = BSP_MAX98357A_Deinit();
    if (ret != ESP_OK) {
        return ret;
    }
    return deinit_ret;
}
